package com.reactiveworks.jsonschema.easpickingv2;

public class Pickable {
	private ExecutionGroup[] executionGroup;
	private Picks[] picks;
	public ExecutionGroup[] getExecutionGroup() {
		return executionGroup;
	}
	public void setExecutionGroup(ExecutionGroup[] executionGroup) {
		this.executionGroup = executionGroup;
	}
	public Picks[] getPicks() {
		return picks;
	}
	public void setPicks(Picks[] picks) {
		this.picks = picks;
	}
	
	 
}
